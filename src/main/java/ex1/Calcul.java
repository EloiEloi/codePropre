package ex1;

/**
 * @author Eloi
 * 
 *         Class représentant des calcul mathématiques simple (addition et soustraction)
 * 
 */
public class Calcul {

	/**
	 * @param valeur1
	 * @param valeur2
	 * @return valeur1+valeur2
	 * 
	 *         Cette méthode retourne le résultat de l'addition des 2 valeurs passées en paramètre
	 * 
	 */
	public int addition(int valeur1, int valeur2) {
		return valeur1 + valeur2;
	}

	/**
	 * @param valeur1
	 * @param valeur1
	 * @return valeur1-valeur2
	 */
	public int soustraction(int valeur1, int valeur2) {
		return valeur1 - valeur2;
	}
}
